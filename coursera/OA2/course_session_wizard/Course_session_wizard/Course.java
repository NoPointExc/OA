class Course{
	long launch_date;
	long enrollment_date;
	int repeat_frequency;
	int duration;

	public Course(long launch_date, long enrollment_date, int repeat_frequency, int duration){
		this.launch_date = launch_date;
		this.enrollment_date = enrollment_date;
		this.repeat_frequency = repeat_frequency;
		this.duration = duration;
	}
}